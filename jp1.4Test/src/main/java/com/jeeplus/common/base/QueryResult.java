package com.jeeplus.common.base;

import java.util.List;

/**
 * <p>
 * <strong>QueryResult</strong> - 查询结果封装，一般用于分页结果
 * </p>
 * 
 * @author liws
 * @version 1.0
 * 
 * @date 2011-05-09 17:28:00
 */
public class QueryResult {
	
	/**
	 * 总记录数
	 */
	private int total;
	
	/**
	 * 结果集
	 */
	private List result;
	
	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}

	public List getResult() {
		return result;
	}

	public void setResult(List result) {
		this.result = result;
	}

	public QueryResult() {}
	
	public QueryResult(List result, int total) {
		this.result = result;
		this.total = total;
	}
	
}
